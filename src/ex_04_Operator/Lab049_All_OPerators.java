package ex_04_Operator;

public class Lab049_All_OPerators {
    public static void main(String[] args){
        int a =20;
        int b= 30;

        System.out.println(a+b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);

        // Arithematic Operators
        //+,-,*,%,/

        // Compound Assignement Operators

        int age =10;
        age +=10;
        System.out.println(age);
        age /=10;
        System.out.println(age);
        age /=10;
        System.out.println(age);
    }
}
