package ex_04_Operator;

public class Lab039_Operator_Modulus {
    public static void main(String[] args) {
        int a=20;
        int b=10;

        System.out.println(a%b); //% modulus
//        //          10/20/ 2-> Quotient
//                    / 20/
//                ______________
//                0-> Reminder

        int a1=7;
        int b1=13;

        System.out.println(a1%b1); //% modulus
////        //          7/13/ 1-> Quotient
////                    / 7/
//                    Reminder- 6
//                ______________
//                0-> Reminder
    }
}
